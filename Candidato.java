public class Candidato {

    // Declaração de atributos
    private String nomeCandidato;
    private String nomePartido;
    private int siglaPartido;
    private int idadeCandidato;
    private String sexoCandidato;
    
    //Get e Set do atributo nomeCandidato
        
    
    public String getNomeCandidato() {
        return nomeCandidato;
    }
    public void setNomeCandidato(String nomeCandidato) {
        this.nomeCandidato = nomeCandidato;
    }
    
    //Get e Set do atributo nomePartido

    public String getNomePartido() {
        return nomePartido;
    }
    public void setNomePartido(String nomePartido) {
        this.nomePartido = nomePartido;
    }

    //Get e Set do atributo siglaPartido
   
    public int getSiglaPartido() {
        return siglaPartido;
    }
    public void setSiglaPartido(int siglaPartido) {
        this.siglaPartido = siglaPartido;
    }
    
    //Get e Set idadeCandidato

    public int getIdadeCandidato() {
        return idadeCandidato;
    }
    public void setIdadeCandidato(int idadeCandidato) {
        this.idadeCandidato = idadeCandidato;
    }
    
    //Get e Set sexoCandidato
    public String getSexoCandidato() {
        return sexoCandidato;
    }
    public void setSexoCandidato(String sexoCandidato) {
        this.sexoCandidato = sexoCandidato;
    }
        
} 



    

